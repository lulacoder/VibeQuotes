export function getQuotesByAuthor(authorSlug: string): ModernQuote[] {
  return modernQuotes.filter((q) => q.authorSlug === authorSlug);
}

export function getQuotesByCategory(category: AuthorCategory): ModernQuote[] {
  return modernQuotes.filter((q) => q.category === category);
}

export function getQuotesByTag(tag: string): ModernQuote[] {
  return modernQuotes.filter((q) => q.tags.includes(tag));
}

export function getRandomModernQuote(): ModernQuote {
  return modernQuotes[Math.floor(Math.random() * modernQuotes.length)];
}

export function getRandomModernQuotes(count: number): ModernQuote[] {
  const shuffled = [...modernQuotes].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

export function searchModernQuotes(query: string): ModernQuote[] {
  const lowerQuery = query.toLowerCase();
  return modernQuotes.filter(
    (q) =>
      q.content.toLowerCase().includes(lowerQuery) ||
      q.author.toLowerCase().includes(lowerQuery) ||
      q.tags.some((t: string) => t.toLowerCase().includes(lowerQuery))
  );
}
